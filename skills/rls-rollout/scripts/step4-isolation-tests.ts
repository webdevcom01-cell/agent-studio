#!/usr/bin/env tsx
/**
 * step4-isolation-tests.ts — Generate cross-tenant isolation test stubs.
 *
 * For each table in a given phase, generates a Vitest test stub covering:
 *   - orgA cannot SELECT orgB's rows
 *   - orgA cannot UPDATE orgB's rows
 *   - orgA cannot DELETE orgB's rows
 *   - orgA INSERT is rejected when organizationId mismatch
 *
 * Output files are placed in skills/rls-rollout/tests/generated/.
 * Tests are marked .skip by default — unskip after applying migrations to staging.
 *
 * Usage:
 *   pnpm tsx skills/rls-rollout/scripts/step4-isolation-tests.ts --phase=1
 *   pnpm tsx skills/rls-rollout/scripts/step4-isolation-tests.ts --table=Agent
 */

