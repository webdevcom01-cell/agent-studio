function run(): void {
  const args = process.argv.slice(2);
  const phaseArg = args.find((a) => a.startsWith("--phase="))?.split("=")[1];
  const tableArg = args.find((a) => a.startsWith("--table="))?.split("=")[1];

  let tables: TableSpec[] = [];

  if (phaseArg === "1") {
    tables = PHASE_1_TABLES;
  } else if (tableArg) {
    const found = PHASE_1_TABLES.find((t) => t.name === tableArg);
    if (!found) {
      console.error(`Unknown table: ${tableArg}. Add it to PHASE_1_TABLES or update this script.`);
      process.exit(1);
    }
    tables = [found];
  } else {
    console.error("Usage: --phase=1  OR  --table=<ModelName>");
    process.exit(1);
  }

  mkdirSync(OUT_DIR, { recursive: true });

  for (const table of tables) {
    const outFile = resolve(OUT_DIR, `${table.name.toLowerCase()}.isolation.test.ts`);
    writeFileSync(outFile, generateTestFile(table));
    console.log(`✓ Generated: tests/generated/${table.name.toLowerCase()}.isolation.test.ts`);
  }

  console.log(
    `\n${tables.length} test stubs generated in skills/rls-rollout/tests/generated/`
  );
  console.log("Tests are marked .skip — unskip after applying migrations to staging.");
}

run();
