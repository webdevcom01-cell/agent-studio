/**
 * Convert a structured CodeGenOutput into human-readable markdown.
 *
 * Used in two places:
 *   1. As `stepOutput` for context accumulation — planning/review agents read this.
 *   2. As `lastImplOutput` fed into the feedback loop — the feedback agent reads it.
 *
 * We keep the format close to what `parseCodeBlocks` would produce from free-form
 * AI text so that any code downstream that reads these outputs stays consistent.
 */
function serializeCodeGenOutput(output: import("./schemas").CodeGenOutput): string {
  const sections: string[] = [`## Summary\n\n${output.summary}`];

  for (const file of output.files) {
    const verb = file.isNew ? "New" : "Modified";
    sections.push(
      `### ${verb}: \`${file.path}\`\n\`\`\`${file.language}\n${file.content}\n\`\`\``,
    );
  }

  const deps = output.dependencies ?? [];
  if (deps.length > 0) {
    const depList = deps
      .map((d) => `- \`${d.name}@${d.version}\`${d.isDev ? " (dev)" : ""}`)
      .join("\n");
    sections.push(`### Dependencies\n\n${depList}`);
  }

  const envVars = output.envVariables ?? [];
  if (envVars.length > 0) {
    const envList = envVars
      .map((e) => `- \`${e.key}\` — ${e.description}${e.required ? " *(required)*" : ""}`)
      .join("\n");
    sections.push(`### Environment Variables\n\n${envList}`);
  }

  if (output.prismaSchemaChanges) {
    sections.push(`### Prisma Schema Changes\n\n\`\`\`prisma\n${output.prismaSchemaChanges}\n\`\`\``);
  }

  return sections.join("\n\n");
}

/**
 * Build the context document from accumulated parts, respecting MAX_CONTEXT_CHARS.
 *
 * Strategy:
 *   1. Task description (parts[0]) is never trimmed — always included in full.
 *   2. The PROTECTED_RECENT_STEPS most-recent step outputs are guaranteed to be
 *      included at their full stored size (up to CONTEXT_SLICE_PER_STEP chars each).
 *      This ensures the implementation and test steps that immediately precede the
 *      current step are always visible to the AI in full — they carry the most
 *      critical continuity information.
 *   3. Remaining budget is distributed across older steps newest-first, with
 *      aggressive trimming once the budget is exhausted.
 *
 * Exported for unit testing.
 */
/**
 * @param parts       The full contextParts array: [task, (priorMemory?), step-0, step-1, …]
 * @param preambleCount  How many leading slots are "preamble" (never trimmed).
 *                    Pass 1 when no priorMemory, 2 when priorMemory is present.
 *                    Defaults to 1 for backward compatibility.
 */
export function buildContextDoc(parts: string[], preambleCount = 1): string {
  const joined = parts.join("\n\n---\n\n");
  if (joined.length <= MAX_CONTEXT_CHARS) return joined;

  // Preamble = task + optional priorMemory — always kept at full length, never trimmed.
  const preamble = parts.slice(0, preambleCount);
  const stepParts = parts.slice(preambleCount); // actual step outputs only
  const n = stepParts.length;
  const allocated: string[] = new Array<string>(n);

  // ── Phase 1: lock in the most-recent PROTECTED_RECENT_STEPS steps ──────────
  // These are never sacrificed regardless of total context size.
  const protectedCount = Math.min(PROTECTED_RECENT_STEPS, n);
  let protectedCharsUsed = 0;

  for (let i = n - protectedCount; i < n; i++) {
    // Each protected step is already truncated at CONTEXT_SLICE_PER_STEP when
    // it was pushed into contextParts, so we include it verbatim.
    allocated[i] = stepParts[i];
    protectedCharsUsed += stepParts[i].length;
  }

  // ── Phase 2: distribute remaining budget over older steps newest-first ──────
  // Separator overhead: "\n\n---\n\n" = 9 chars; rough estimate with parts count.
  const separatorOverhead = parts.length * 9;
  const preambleLength = preamble.reduce((sum, p) => sum + p.length, 0);
  let remaining =
    MAX_CONTEXT_CHARS - preambleLength - protectedCharsUsed - separatorOverhead;

  for (let i = n - protectedCount - 1; i >= 0; i--) {
    if (remaining <= 0) {
      // Budget exhausted — keep only the header line so the AI knows the step ran
      allocated[i] = stepParts[i].split("\n")[0] + "\n[context trimmed — budget exhausted]";
    } else if (stepParts[i].length <= remaining) {
      allocated[i] = stepParts[i];
      remaining -= stepParts[i].length;
    } else {
      allocated[i] = stepParts[i].slice(0, remaining) + "\n…[trimmed]";
      remaining = 0;
    }
  }

  return [...preamble, ...allocated].join("\n\n---\n\n");
}

function buildSummary(outputs: string[], pipeline: string[]): string {
  if (outputs.length === 0) return "No steps completed.";

  const sections = outputs.map((out, i) => {
    const stepId = pipeline[i] ?? `step-${i}`;
    const snippet = out.length > 1500 ? `${out.slice(0, 1500)}\n…[truncated]` : out;
    return `## Step ${i + 1}: ${stepId}\n\n${snippet}`;
  });

  return `# Pipeline Execution Summary\n\n${sections.join("\n\n---\n\n")}`;
}


// ── Gate step helper ──────────────────────────────────────────────────────────

export interface RunSingleGateStepInput {
  stepId: string;
  stepIdx: number;
  /** Read-only snapshot of accumulated context parts at call time. */
  contextParts: string[];
  /** Value of CONTEXT_STEP_OFFSET from the enclosing runPipeline call. */
  contextOffset: number;
  codebaseReady: boolean;
  taskDescription: string;
  resolvedModelId: string;
  stepModelOverrides: Record<string, string>;
  agentId: string;
  userId: string | undefined;
  runId: string;
  useSmartRouting: boolean;
  adaptiveStatsCache: Map<string, StatRow[]> | undefined;
  /** Parent OTel span — used only to read traceContext for child span creation. */
  pipelineSpan: import("@/lib/observability/tracer").Span;
  /** Caller-supplied abort signal (e.g. AbortSignal.timeout(getStepTimeoutMs(stepId))). */
  abortSignal: AbortSignal;
}

/**
 * Execute a single gate step (ecc-code-reviewer / ecc-security-reviewer) in isolation.
 *
 * Designed to be called sequentially (Prompt 1) or in parallel via
 * Promise.allSettled (Prompt 2).  The function is intentionally side-effect-free
 * with respect to shared orchestrator state: it does NOT mutate stepOutputs,
 * contextParts, totalInputTokens, or stepMetricsMap — all merging is the caller's
 * responsibility.
 *
 * Non-mutating side effects that ARE performed here: OTel child span, recordTokenUsage,
 * recordChatLatency, fireSdkLearnHook (all fire-and-forget or scoped to this step).
 */
export async function runSingleGateStep(input: RunSingleGateStepInput): Promise<GateStepResult> {
  const {
    stepId, stepIdx,
    contextParts, contextOffset,
    codebaseReady, taskDescription, resolvedModelId, stepModelOverrides,
    agentId, userId, runId, useSmartRouting, adaptiveStatsCache,
    pipelineSpan, abortSignal,
  } = input;

  // Dynamic imports — modules are cached after first load, negligible overhead.
  const { fireSdkLearnHook } = await import("@/lib/ecc/sdk-learn-hook");
  const { getAgentSystemPrompt } = await import("./agent-prompts");
  const { getModel } = await import("@/lib/ai");
  const { generateText, generateObject } = await requireAi();

  const phase: StepPhase = "review";
  const stepModelId = useSmartRouting
    ? await resolveStepModelAdaptive(phase, stepModelOverrides, stepId, resolvedModelId, adaptiveStatsCache)
    : resolveStepModel(phase, stepModelOverrides, stepId, resolvedModelId, false);
  const model = getModel(stepModelId);

  const stepSpan = startSpan("sdlc.pipeline.step", {
    kind: "client",
    parentContext: pipelineSpan.traceContext,
    attributes: {
      "sdlc.step.id": stepId,
      "sdlc.step.index": stepIdx,
      "sdlc.step.phase": phase,
      "gen_ai.request.model": stepModelId,
    },
  });

  logger.info("Pipeline: model resolved for step", {
    runId, stepIdx, stepId, stepModelId, phase,
    isOverride: !!stepModelOverrides[stepId],
    isSmartRouted: useSmartRouting && !stepModelOverrides[stepId],
  });

  const systemPrompt = getAgentSystemPrompt(stepId);
  const stepStart = Date.now();

  // ── RAG: retrieve code relevant to this gate step ─────────────────────────
  let codeContext = "";
  if (codebaseReady) {
    try {
      const ragQuery = `${stepId} ${taskDescription.slice(0, 200)}`;
      const ragResults = await searchCodebase(ragQuery, agentId, RAG_TOP_K_DEFAULT);
      codeContext = buildCodeContext(ragResults);
      logger.info("Pipeline: RAG search complete", {
        runId, stepIdx, stepId,
        ragTopK: RAG_TOP_K_DEFAULT,
        resultsReturned: ragResults.length,
      });
    } catch (err) {
      logger.warn("Pipeline: RAG search failed for step", {
        runId, stepIdx, stepId,
        error: err instanceof Error ? err.message : String(err),
      });
    }
  }

  // ── Prompt assembly ───────────────────────────────────────────────────────
  const contextDoc = buildContextDoc(contextParts, contextOffset);
  const promptSections: string[] = [
    contextDoc,
    ...(codeContext ? [`\n\n---\n\n${codeContext}`] : []),
    `\n\n---\n\n# Instructions for this step (${stepId})`,
    `Apply your expertise to the task above. Be specific, actionable, and thorough.`,
    `Reference and build upon any previous step outputs shown in the context above.`,
  ];
  const prompt = promptSections.join("\n");

  // ── Structured output — gate steps always prefer generateObject() ─────────
  const gateSchema = stepId === "ecc-security-reviewer"
    ? SecurityReviewOutputSchema
    : CodeReviewOutputSchema;

  let stepOutput: string;
  let inputTokens = 0;
  let outputTokens = 0;

  try {
    const result = await generateObject({
      model,
      system: systemPrompt,
      prompt,
      schema: gateSchema,
      maxOutputTokens: 4096,
      abortSignal,
      providerOptions: { openai: { strictJsonSchema: false } },
    });
    stepOutput   = JSON.stringify(result.object, null, 2);
    inputTokens  = result.usage.inputTokens  ?? 0;
    outputTokens = result.usage.outputTokens ?? 0;
  } catch (gateErr) {
    if (gateErr instanceof Error && gateErr.name === "AbortError") throw gateErr;
    logger.warn("Pipeline: gate generateObject failed, falling back to generateText", {
      runId, stepIdx, stepId,
      error: gateErr instanceof Error ? gateErr.message : String(gateErr),
    });
    const fallback = await generateText({
      model,
      system: systemPrompt,
      prompt,
      maxOutputTokens: 4096,
      abortSignal,
    });
    stepOutput   = fallback.text;
    inputTokens  = fallback.usage.inputTokens  ?? 0;
    outputTokens = fallback.usage.outputTokens ?? 0;
  }

  // ── Gate decision: BLOCK halts the pipeline (handled by caller) ───────────
  let blocked = false;
  let blockSummary: string | undefined;
  try {
    const parsed = JSON.parse(stepOutput) as { decision?: string; summary?: string };
    blocked      = parsed.decision === "BLOCK";
    blockSummary = parsed.summary;
  } catch {
    // generateText fallback produced non-JSON — cannot extract decision.
    // Treat as APPROVE so the pipeline can continue; the full text is stored.
  }

  const stepDurationMs = Date.now() - stepStart;

  // ── Non-mutating telemetry side effects ───────────────────────────────────
  recordTokenUsage(agentId, stepModelId, inputTokens, outputTokens);
  recordChatLatency(agentId, stepModelId, stepDurationMs);

  void fireSdkLearnHook({
    agentId,
    userId,
    task: `[${stepId}] ${taskDescription.slice(0, 500)}`,
    response: stepOutput.slice(0, 2000),
    modelId: stepModelId,
    durationMs: stepDurationMs,
    inputTokens,
    outputTokens,
  });

  const stepMetric: StepMetric = {
    stepId,
    phase,
    modelId: stepModelId,
    inputTokens,
    outputTokens,
    durationMs: stepDurationMs,
    feedbackAttempts: 0,
    outcome: "success",
  };

  stepSpan.setAttributes({
    "gen_ai.usage.input_tokens":    inputTokens,
    "gen_ai.usage.output_tokens":   outputTokens,
    "sdlc.step.duration_ms":        stepDurationMs,
    "sdlc.step.outcome":            "success",
    "sdlc.step.feedback_attempts":  0,
  });
  stepSpan.end();

  return {
    stepId, stepIdx,
    output: stepOutput,
    inputTokens, outputTokens,
    blocked, blockSummary,
    stepMetric,
  };
}


/**
 * Run two (or more) gate steps concurrently using Promise.allSettled.
 *
 * Results are returned in the same order as the input array so the caller
 * can merge into stepOutputs[] in pipeline order without any sorting.
 * If any step throws (e.g. AbortError / network error), that error is
 * re-thrown after the settled batch so the pipeline fails cleanly.
 */
export async function runParallelGateSteps(
  inputs: RunSingleGateStepInput[],
): Promise<GateStepResult[]> {
  const settled = await Promise.allSettled(inputs.map(inp => runSingleGateStep(inp)));

  // Surface the first non-BLOCK rejection (AbortError, timeout, etc.)
  for (const s of settled) {
    if (s.status === "rejected") {
      throw s.reason as Error;
    }
  }

  // All fulfilled — return in input order
  return settled.map(s => (s as PromiseFulfilledResult<GateStepResult>).value);
}
